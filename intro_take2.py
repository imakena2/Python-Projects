# Integer (Whole number)
age = 30

# Float (Decimal number)
height = 7.8945

# String (Text) - Add quotes
name = "Idah"

# Boolean (True/False)
student = True

# Check Data Type
print(type(age))      # Output: <class 'int'>
print(type(height))   # Output: <class 'float'>
print(type(name))     # Output: <class 'str'>
print(type(student))  # Output: <class 'bool'>